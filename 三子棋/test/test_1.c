#define _CRT_SECURE_NO_WARNINGS 1
#include"game.h"
//测试三子棋
void menu()
{
	printf("****************\n");
	printf("* 1.play 0.exit*\n");
	printf("****************\n");
}
//游戏整个算法的实现
void game()
{
	char ret = 0;
	//数组-存放走出的棋子信息
	char board[ROW][COL] = { 0 };//头文件改变，则这里数组的大小也会改变

	//初始化棋盘，使打印出的棋盘之间全部为空格
	InitBoard(board, ROW, COL);

	//打印棋盘的具体函数，怎么打印
	DisplayBoard(board, ROW, COL);
	//下棋
	while (1)
	{
		//玩家下棋
		PlayMove(board,ROW,COL);
		DisplayBoard(board, ROW, COL);
		//判断玩家输赢
		ret = IsWin(board, ROW, COL);
		if (ret != 'C')
		{
			break;
		}
		//电脑下棋
		ComputerMove(board, ROW, COL);
		DisplayBoard(board, ROW, COL);
		//判断电脑输赢
		ret = IsWin(board, ROW, COL);
		if (ret != 'C')
		{
			break;
		}
	}
	if (ret == '*')
	{
		printf("玩家赢\n");
	}
	else if (ret == '#')
	{
		printf("电脑赢\n");
	}
	else
	{
		printf("平局\n");
	}
}
void test()
{
	int input = 0;
	srand((unsigned int)time(NULL));//只能调用一次，test只用了一次，所以放在这里
	do
	{
		menu();
		printf("请选择:>");
		scanf("%d", & input);
		switch (input)
		{
		case 1:
			game();
			break;
		case 0:
			printf("退出游戏\n");
			break;
		default:
			printf("选择错误，请重新选择：\n");
		}
	} while (input);
}
int main()
{
	test();
	return 0;
}	